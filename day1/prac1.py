# 기본 자료형 및 연산 확인
a = 1000                    # 양의 정수
print(a)

b = -7                      # 음의 정수
print(-7)

c = 0                       # 0
print(c)

print(b+c)                  # 덧셉
print(c-b)                  # 뺄셈

d = 75.25e-2                # 지수표현 -> (유효숫자)e(지수)
print(d)

e = 22.2                    # 실수
f = round(e)                # 반올림
print(f)

g = 2
h = 3
print(g**h)                 # 제곱

x = 7
y = 3
print(x*y)                  # 곱셈
print(y/x)                  # 나눗셈
result = round(y/x, 2)      # 자리수가 표시 된 반올림
print(result)


